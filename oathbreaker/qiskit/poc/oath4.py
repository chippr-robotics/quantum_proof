    print("order n = %d (prime)" % GROUP_ORDER)
    print("generator G = %s\n" % (GENERATOR,))
    print("index  point")
    for i, pt in enumerate(all_points()):
        marker = "  <-- generator" if i == 1 else ""
        print(f"  {i:2d}    {pt}{marker}")
    inst = Instance.from_secret(7)
    print(f"\nexample instance: k={inst.k}, Q={inst.Q}")
    print(f"classical dlog recovers k = {classical_dlog(inst.Q)}")
